    private void checkforHex() {
        
        File file = new File(hexPath);
        if(file.exists() == false){
            setStatus("Target hex file not found in the expected location.", Color.RED,false);
        }
        else{
            setStatus("Target hex file is available. Ready to encrypt!", Color.BLUE, false);
        }
    }

    private void setStatus(String msg, Color color, boolean m) {
       lblMessage.setText(msg);
       lblMessage.setForeground(color);
    }

    private void Encrypt() {
         Thread te = new Thread(new Runnable() {

        public void run()
        {
            controlAllButtons(false);
             if(PwdTxtField.getText().compareTo(ReenterPwdTxtField.getText())==0){
            if(PwdTxtField.getText().length()>=4){
            if(jTextField1.getText().contains(".hex")==false){
                setStatus("Hex file not chosen.", Color.RED, false);
            }
            if(doEncrypt()){

            setStatus("The tareget hex file is secure!", Color.BLUE,false);
            JOptionPane.showMessageDialog(hexencrypter.hexcrypter.this.getRootPane(), "Successfully generated encrypted target hex file!", "Encrypted", JOptionPane.OK_OPTION);
            System.exit(0);
            }
             else{
                 setStatus("Encryption failed!", Color.RED,false);
             }
            }
            else {
               setStatus("Please provide minimum of 4 characters", Color.RED,false);
            }

            }

        else{
                setStatus("Passwords are not matching. Please try again", Color.RED,false);
        }
            controlAllButtons(true);
        }});
        te.start();
    }

    
    private boolean doEncrypt(){
        boolean retVal = true;
            AESCrypt encrpter = null;
            try {
                hexPath = jTextField1.getText();
                encrpted = hexPath.replace(".hex", "_encrypted.hex");
                encrpter = new AESCrypt(PwdTxtField.getText());
                encrpter.encrypt(2, hexPath, encrpted);
                //String dec = System.getProperty("user.home")+"/target_decrypted.hex";
               //encrpter.decrypt(encrpted, dec);
            } catch (GeneralSecurityException ex) {
                retVal = false;
            } catch (UnsupportedEncodingException ex) {
                retVal = false;
            }catch (IOException ex) {
                retVal = false;
            }
            return retVal;
    }

