    private void importHex(){
        String path = System.getProperty("user.home");
        final JFileChooser projectChooser = new JFileChooser(path);
        ExtensionFileFilter filter1 =  new ExtensionFileFilter("Hex (Hex File)", new String[] {"HEX"});
        projectChooser.setFileFilter (filter1);
        projectChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        int status=projectChooser.showOpenDialog(null);
        if(status==JFileChooser.APPROVE_OPTION)
        {
         jTextField1.setText(projectChooser.getSelectedFile().getAbsolutePath());
        }
        else if(status==JFileChooser.CANCEL_OPTION){
            setStatus("No hex file selected.\n", Color.RED,true);
        }
    }

